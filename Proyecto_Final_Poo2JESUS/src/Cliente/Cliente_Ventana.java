    private void btnEliminarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarClienteActionPerformed
        // TODO add your handling code here:
        try
        {
            int confirmed = JOptionPane.showConfirmDialog(null, 
            "Esta seguro de elimnar este cliente?", "Mensaje de Avertencia",
           JOptionPane.YES_NO_OPTION);

            if (confirmed == JOptionPane.YES_OPTION)
            {
                String codigoSeleccionado   = cboBoxIDEliminar.getSelectedItem().toString();
                Cliente objEliminarCliente = clienteVectores.buscarcodigo(codigoSeleccionado);
                if ( objEliminarCliente.getCodigoCliente().equals("Error"))
                {
                    JOptionPane.showMessageDialog(null, "No se encontro el codigo");
                }
                else
                {
                    clienteVectores.eliminar(objEliminarCliente);
                    clienteVectores.grabarModificareliminar();
                    JOptionPane.showMessageDialog(null, "Cliente eliminado satisfactoriamente");
                }
                
            }
            else
            {
                JOptionPane.showMessageDialog(null, "Operacion cancelada");
            }
        }
        catch(Exception e)
        {
            System.out.println("Ocurrio una excepcion en btnEleiminar: " + e);
        }
    }//GEN-LAST:event_btnEliminarClienteActionPerformed
    
